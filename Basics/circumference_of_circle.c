// program to determine the circumference of circle.

#include<stdio.h>
int main()
{
 float r,cmf;
 printf("enter radius of circle:");
 scanf("%f",&r);
 cmf=2*3.14159*r;                 // circumference of circle = 2*pi*radius.
 printf("circumference of circle is %f",cmf);
 return 0;
}

